#include<iostream>
using namespace std;
int main()
{
    int a = 10;
    float f = 10.5;
    double d = 52.69;
    char ch = 'A';
    string str = "Hello World";
    bool b1 = true; 
    bool b2 = false;
    cout<<"Value of integer data type = "<<a<<endl;
    cout<<"Value of float data type = "<<f<<endl;
    cout<<"Value of double data type = "<<d<<endl;
    cout<<"Value of character data type = "<<ch<<endl;
    cout<<"Value of string data type = "<<str<<endl;
    cout<<"Value of boolean data type = "<<b1<<endl;
    cout<<"Value of boolean data type = "<<b2;
    return 0;
}